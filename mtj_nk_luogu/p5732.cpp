#include <stdio.h>
int main(void) {
    int n, i, j;
    scanf("%d", &n);  // 先获取n的值，再定义数组
    int y[n][n];      // 变长数组（VLA）不能初始化，需后续手动赋值

    // 初始化杨辉三角的第一列和对角线为1
    for (i = 0; i < n; i++) {
        y[i][0] = 1;      // 第一列（j=0）始终为1
        y[i][i] = 1;      // 对角线（i=j）始终为1
    }

    // 填充中间元素（从第2行开始，第1列到第i-1列）
    for (i = 2; i < n; i++) {  // i从2开始（第0、1行已初始化）
        for (j = 1; j < i; j++) {  // j范围：1到i-1（避开第一列和对角线）
            y[i][j] = y[i-1][j-1] + y[i-1][j];
        }
    }

    // 打印杨辉三角（按行打印，每个元素后空格分隔）
    for (i = 0; i < n; i++) {
        for (j = 0; j <= i; j++) {  // 每行打印i+1个元素（j从0到i）
            printf("%d ", y[i][j]);
        }
        printf("\n");  // 每行结束后换行（原代码用空格，会导致格式错乱）
    }

    return 0;
}

