#include<stdio.h>
int main(void) {
    int out[12];  // 每月支出
    int l = 0, s = 0;  // l：当月剩余金额，s：累计存款
    int i;

    for (i = 0; i < 12; i++) {  // 修正：从0开始遍历数组
        scanf("%d", &out[i]);
        l += 300 - out[i];  // 每月收入300元，减去支出

        // 若当月余额不足，输出月份并终止
        if (l < 0) {
            printf("-%d\n", i + 1);  // 月份从1开始，修正格式符为%d
            return 0;  // 直接结束程序，无需继续循环
        }

        // 若有剩余金额，整百部分存入银行
        if (l >= 100) {
            int save = (l / 100) * 100;  // 可存款金额（整百部分）
            s += save;                   // 累计存款
            l -= save;                   // 剩余零钱（不足100元部分）
        }
    }

    // 年末计算利息（存款*1.2倍，即20%利息）
    printf("%.0f\n", (double)s * 1.2);  // 修正：用%f打印浮点数
    return 0;
}

